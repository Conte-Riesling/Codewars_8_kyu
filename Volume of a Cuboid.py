# Bob needs a fast way to calculate the volume of a cuboid with three values: \n
# the length, width and height of the cuboid. Write a function to help Bob with this calculation.

# Бобу нужен быстрый способ вычисления объема прямоугольного параллелепипеда с тремя значениями: \n
# длина, ширина и высота прямоугольного параллелепипеда. Напишите функцию, \n
# которая поможет Бобу в этом вычислении.

def get_volume_of_cuboid(length, width, height):
    return length * width * height